package Assignments;

public class ForEvenNumber {

	public static void main(String[] args) {


		//int number=10;
		for(int i=1;i<=20;i++)
		{
			if (i % 2 == 0) {
				 
	          System.out.println( i + " Number is Even" );
	      }
	
	      else {
	
	          System.out.println( i + "Number is Odd");
	      	}
    
		}
	}

}
