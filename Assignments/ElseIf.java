package Assignments;

public class ElseIf {

	public static void main(String[] args) {
	
		String dest1="Pune";
		String dest2="Mumbai";


		if(dest1=="Pune") {
			System.out.println("Pune ticket cost=100");
		}
		else if(dest2=="Mumbai") {
			System.out.println("Mumbai ticket cost=200");
		}
		else {
			System.out.println("No fair..");
			
		}
	}

}
