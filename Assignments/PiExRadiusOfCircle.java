package Assignments;

public class PiExRadiusOfCircle {

	public static void main(String[] args) {


		double area,circf;
		int radius = 5;
		int circumference=5;
		
		area= Math.PI*radius*radius;  
		System.out.println("The area of the circle is: "+area);
		
		circf=(circumference/2*Math.PI);
		System.out.println("The radius of the circle is: "+circf);
		
	}

}
